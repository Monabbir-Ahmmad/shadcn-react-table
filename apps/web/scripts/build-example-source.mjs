// Extracts the source of each live example in components/examples/registry.tsx
// (keyed by slug) so the docs <Example> component can show a "Code" tab next to
// the live demo — no per-example sandbox files needed. Run from the repo root
// (anchored via import.meta.url):
//   node apps/web/scripts/build-example-source.mjs
import { writeFileSync } from "node:fs"
import { join, dirname } from "node:path"
import { fileURLToPath } from "node:url"
import { Project, SyntaxKind } from "ts-morph"

const WEB = join(dirname(fileURLToPath(import.meta.url)), "..")
const REGISTRY = join(WEB, "components/examples/registry.tsx")
const OUT = join(WEB, "lib/example-source.generated.ts")

const project = new Project({
  skipAddingFilesFromTsConfig: true,
  compilerOptions: { allowJs: false, jsx: 4 },
})
const sf = project.addSourceFileAtPath(REGISTRY)

// name -> verbatim source text for every top-level function declaration.
const functions = new Map()
for (const fn of sf.getFunctions()) {
  const name = fn.getName()
  if (name) functions.set(name, fn.getText().trim())
}

/**
 * Resolve the component an EXAMPLES entry renders. Either a direct identifier
 * (`Component: DensityExample`) or a thin arrow wrapper
 * (`Component: () => <EditingExample mode="cell" />`). Returns
 * `{ name, invocation }` where invocation is the wrapper JSX when present.
 */
function resolveComponent(initializer) {
  if (initializer.getKind() === SyntaxKind.Identifier) {
    return { name: initializer.getText(), invocation: null }
  }
  // Arrow function -> find the JSX element it returns.
  const jsx =
    initializer.getFirstDescendantByKind(SyntaxKind.JsxSelfClosingElement) ??
    initializer.getFirstDescendantByKind(SyntaxKind.JsxOpeningElement)
  if (jsx) {
    const tag = jsx.getTagNameNode().getText()
    return { name: tag, invocation: jsx.getText() }
  }
  return { name: null, invocation: null }
}

// Find the EXAMPLES array and walk its object-literal entries.
const examplesDecl = sf.getVariableDeclarationOrThrow("EXAMPLES")
const arr = examplesDecl.getInitializerIfKindOrThrow(
  SyntaxKind.ArrayLiteralExpression
)

const sources = {}
let missing = 0
for (const el of arr.getElements()) {
  if (el.getKind() !== SyntaxKind.ObjectLiteralExpression) continue
  const slugProp = el.getProperty("slug")
  const compProp = el.getProperty("Component")
  if (!slugProp || !compProp) continue
  const slug = slugProp
    .getInitializerOrThrow()
    .getText()
    .replace(/^["'`]|["'`]$/g, "")
  const { name, invocation } = resolveComponent(compProp.getInitializerOrThrow())
  const body = name ? functions.get(name) : null
  if (!body) {
    console.warn(`  [example-source] no source for "${slug}" (component ${name})`)
    missing++
    continue
  }
  // Prepend the wrapper invocation as a comment so variant examples
  // (e.g. <EditingExample mode="cell" />) read clearly.
  sources[slug] = invocation ? `// ${invocation}\n${body}` : body
}

const banner =
  "// AUTO-GENERATED by apps/web/scripts/build-example-source.mjs — do not edit by hand.\n" +
  "// Regenerate with `pnpm --filter shadcn-react-table-web examples:build`.\n\n"

const file = (
  banner +
  "export const EXAMPLE_SOURCE: Record<string, string> = " +
  JSON.stringify(sources, null, 2) +
  "\n"
  // Normalize CRLF that JSON.stringify escaped into the string literals when
  // the registry source was checked out with Windows line endings (git
  // autocrlf), so the artifact is identical on every machine.
).replaceAll("\\r\\n", "\\n")

writeFileSync(OUT, file)
console.log(
  `example sources built → apps/web/lib/example-source.generated.ts (${
    Object.keys(sources).length
  } examples${missing ? `, ${missing} missing` : ""})`
)
